function draw() {
    context.clearRect(0, 0, width, height);
    prev_x = snake.x;
    prev_y = snake.y;
    grid[snake.y][snake.x] = 1;
    // grid[prev_y][prev_x] = 0;
    let row_number = 0;
    for (let row of grid) {
        let cell_number = 0;
        for (let cell of row) {
            if (cell === 1) {
                context.fillRect((cell_number*size)+2, (row_number*size)+2, size-2, size-2)
            }
            cell_number++;
        }
        row_number++
    }
    if (moveUp) {
        snake.y--;
    } else if (moveDown) {
        snake.y++;
    } else if (moveLeft) {
        snake.x--;
    } else if (moveRight) {
        snake.x++;
    }
    if (grid[snake.y][snake.x] === 1) {
        stop()
    }
    length++;
}

function activate(event) {
    let KeyCode = event.keyCode;
    if (KeyCode === 37) {
        moveLeft = true;
        moveUp = false;
        moveDown = false;
        moveRight = false;
    } else if (KeyCode === 38) {
        moveUp = true;
        moveDown = false;
        moveLeft = false;
        moveRight = false;
    } else if (KeyCode === 39) {
        moveRight = true;
        moveUp = false;
        moveDown = false;
        moveLeft = false;
    } else if (KeyCode === 40) {
        moveDown = true;
        moveUp = false;
        moveLeft = false;
        moveRight = false;
    }
}

function stop() {
    window.clearInterval(intervalId);
}

function getRandomNumber(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}
